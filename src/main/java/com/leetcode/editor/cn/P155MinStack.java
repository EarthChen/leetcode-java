    class MinStack {


        private LinkedList<Integer> stack = new LinkedList<>();

        private LinkedList<Integer> minStack = new LinkedList<>();

        public MinStack() {

        }

        public void push(int val) {
            stack.push(val);
            if (minStack.isEmpty()) {
                minStack.push(val);
            } else {
                int curMin = minStack.peek();
                minStack.push(Math.min(curMin, val));
            }
        }

        public void pop() {
            stack.pop();
            minStack.pop();
        }

        public int top() {
            return stack.peek();

        }

        public int getMin() {
            return minStack.peek();
        }
    }

