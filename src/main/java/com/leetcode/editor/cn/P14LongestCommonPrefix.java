    class Solution {
        public String longestCommonPrefix(String[] strs) {
            if (strs == null || strs.length == 0) {
                return "";
            }
            String prefix = strs[0];
            int len = prefix.length();
            StringBuilder sb = new StringBuilder();
            for (int i = 0; i < len; i++) {
                char chr = prefix.charAt(i);
                for (int j = 1; j < strs.length; j++) {
                    String str = strs[j];
                    if (i >= str.length()) {
                        return sb.toString();
                    }
                    if (str.charAt(i) != chr) {
                        return sb.toString();
                    }
                }
                sb.append(chr);
            }
            return sb.toString();
        }
    }
