    class Solution {
        public int leastInterval(char[] tasks, int n) {
            if (n == 0) {
                return tasks.length;
            }
            int[] taskCount = new int[26];
            for (char c : tasks) {
                taskCount[c - 'A']++;
            }
            int max = 0;//Âêå‰∏Ä‰∏™‰ªªÂä°ÊâßË°åÊ¨°Êï∞ÁöÑÊúÄÂ§ßÂÄº
            int maxCount = 0;//ÊâßË°å‰∫ÜmaxÊ¨°ÁöÑ‰ªªÂä°ÊúâÂ§öÂ∞ë‰∏™
            for (int i = 0; i < 26; i++) {
                if (taskCount[i] > max) {
                    max = taskCount[i];
                    maxCount = 1;
                } else if (max == taskCount[i]) {
                    maxCount++;
                }
            }
            return Math.max((max - 1) * (n + 1) + maxCount, tasks.length);
        }
    }
